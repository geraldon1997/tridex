<h1>Guide</h1>

<ul>
    <li>Click on get started</li>
    <li>signup with your valid email and password</li>
    <li>verify your account from the link sent to your email</li>
    <li>visit the signin page and enter your credentials</li>
    <li>a login code will be sent to your registered email</li>
    <li>copy the code from your email and go back to the site and paste it in.</li>
    <li>if the code is correct, you will be redirected to your dashboard</li>
    <li>in your dashboard, you have to activate your account by updating your profile</li>
    <li>after updating profile, click on investments and choose either pending or active investments</li>
    <li>you choose a plan and amount and invest</li>
    <li>then you deposit your investment and click on the has paid button</li>
    <li>once your deposit is received, your investment will be activated.</li>
    <li>upon maturiy day, your investment will move from active to completed</li>
    <li>visit your completed investments and withdraw the funds to your wallet</li>
    <li>if you wish to withdraw to your blockchain wallet, visit your site wallet and update your blockchain receiving address</li>
    <li>then request for withdrawal of any amount wanted.</li>
    <li>the withdrawal will be processed and you get paid</li>
</ul>